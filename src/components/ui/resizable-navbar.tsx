export const Navbar = ({ children, className }: NavbarProps) => {
  const [scrollProgress, setScrollProgress] = useState<number>(0);
  const [visible, setVisible] = useState<boolean>(false);

  React.useEffect(() => {
    const handleScroll = () => {
      const scrollY = window.scrollY;
      const startScroll = 50;
      const endScroll = 150;
      
      if (scrollY < startScroll) {
        setScrollProgress(0);
        setVisible(false);
      } else if (scrollY > endScroll) {
        setScrollProgress(1);
        setVisible(true);
      } else {
        const progress = (scrollY - startScroll) / (endScroll - startScroll);
        setScrollProgress(progress);
        setVisible(true);
      }
    };

    // Call once on mount
    handleScroll();

    // Add scroll listener
    window.addEventListener('scroll', handleScroll, { passive: true });
    
    return () => {
      window.removeEventListener('scroll', handleScroll);
    };
  }, []);

  return (
    <motion.div
      className={cn("fixed inset-x-0 top-0 z-40 w-full", className)}
    >
      {React.Children.map(children, (child) =>
        React.isValidElement(child)
          ? React.cloneElement(
              child as React.ReactElement<{ visible?: boolean; scrollProgress?: number }>,
              { visible, scrollProgress },
            )
          : child,
      )}
    </motion.div>
  );
};

export const NavBody = ({ children, className, visible, scrollProgress = 0 }: NavBodyProps) => {
  // Interpolate values based on scroll progress
  const backgroundColor = `rgba(23, 23, 23, ${0.95 * scrollProgress})`;
  const blur = 10 * scrollProgress;
  const yOffset = 20 * scrollProgress;
  const shadowOpacity = scrollProgress;
  
  return (
    <motion.div
      animate={{
        backdropFilter: `blur(${blur}px)`,
        backgroundColor: backgroundColor,
        boxShadow: shadowOpacity > 0 
          ? `0 0 ${24 * shadowOpacity}px rgba(0, 0, 0, ${0.3 * shadowOpacity})`
          : "none",
        y: yOffset,
      }}
      style={{
        width: scrollProgress > 0 ? `${100 - (40 * scrollProgress)}%` : "100%",
        maxWidth: scrollProgress > 0 ? "400px" : "100%",
        paddingLeft: `${32 - (8 * scrollProgress)}px`,
        paddingRight: `${32 - (8 * scrollProgress)}px`,
      }}
      transition={{
        type: "spring",
        stiffness: 300,
        damping: 30,
      }}
      className={cn(
        "relative z-[60] mx-auto hidden w-full max-w-7xl flex-row items-center gap-6 self-start rounded-full bg-transparent px-8 py-3 lg:flex dark:bg-transparent",
        scrollProgress > 0.5 ? "justify-center" : "justify-between",
        className,
      )}
    >
      {children}
    </motion.div>
  );
};

export const NavItems = ({ items, className, onItemClick }: NavItemsProps) => {
  const [hovered, setHovered] = useState<number | null>(null);

  return (
    <motion.div
      onMouseLeave={() => setHovered(null)}
      className={cn(
        "absolute inset-0 hidden flex-1 flex-row items-center justify-center space-x-2 text-sm font-medium text-zinc-600 transition duration-200 hover:text-zinc-800 lg:flex lg:space-x-2",
        className,
      )}
    >
      {items.map((item, idx) => (
        <a
          onMouseEnter={() => setHovered(idx)}
          onClick={onItemClick}
          className="relative px-4 py-2 text-neutral-600 dark:text-neutral-300"
          key={`link-${idx}`}
          href={item.link}
        >
          {hovered === idx && (
            <motion.div
              layoutId="hovered"
              className="absolute inset-0 h-full w-full rounded-full bg-gray-100 dark:bg-neutral-800"
            />
          )}
          <span className="relative z-20">{item.name}</span>
        </a>
      ))}
    </motion.div>
  );
};

